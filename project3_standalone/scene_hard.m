function scene = scene_hard(params)
%SCENE_HARD 困难场景：机械臂保持伸展构型转 120°，沿弧多球挡路
%
% 设计：
%   - q_start 与 q_goal 仅 q1 不同（±60°），q2..q6 选成 "上臂稍抬、
%     前臂直、腕部前指" 的伸展姿态，避免运动到终点时机械臂蜷缩。
%   - q1 扫弧时，末端在任务空间画一段半径 ~576 mm 的水平弧，
%     端点 (288, ±499, 183)，弧顶 (576, 0, 183)。
%   - 沿弧的 s≈0.25 / 0.50 / 0.75 处分别放球（高度上下错开），
%     规划器需要让全臂在不同高度避不同的球，逐个绕过去：
%       s=0.25 → (499,-288,183)  z 抬高到 260
%       s=0.50 → (576,   0,183)  z 压低到 100
%       s=0.75 → (499,+288,183)  z 抬高到 260
%     这样直线穿球；要避开必须让末端走"过、下、过"的曲折轨迹。
%
% 输入：
%     params : 机械臂参数结构体（可选使用）
%
% 输出：
%     scene  : 场景结构体，字段约定与 scene_easy.m 一致

    if nargin < 1
        params = [];
    end 

    scene.name = 'hard';

    q_pose = [0, -pi/6, 0, 0, pi/3, 0];
    scene.q_start = q_pose;  scene.q_start(1) = -pi/3;
    scene.q_goal  = q_pose;  scene.q_goal(1)  = +pi/3;

    % 三球高度上下错开（单位 mm）
    scene.obstacles = [
        499, -288,  260,   80;
        576,    0,  100,   90;
        499,  288,  260,   80
    ];

    scene.T_total = 4.0;
    scene.dt      = 0.02;
end
